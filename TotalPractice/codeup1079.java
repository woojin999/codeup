package TotalPratice;

import java.util.Scanner;

public class codeup1079 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		while (true) {
			char data = sc.next().charAt(0);
			if (data != 'q') {
				System.out.println(data);
			} else {
				System.out.println(data);
				break;
			}
			
		}
	}
}
